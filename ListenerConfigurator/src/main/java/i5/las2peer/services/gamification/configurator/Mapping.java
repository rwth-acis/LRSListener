package i5.las2peer.services.gamification.configurator;

import java.util.List;

public class Mapping {
	private List<GameMapping> gameMapping;
	private List<QuestMapping> questMapping;
	private List<AchievementMapping> achievementMapping;
	private List<BadgeMapping> badgeMapping;
	private List<ActionMapping> actionMapping;
	private List<LevelMapping> levelMapping;
	private List<StreakMapping> streakMapping;
	/**
	 * @return the gameMapping
	 */
	public List<GameMapping> getGameMapping() {
		return gameMapping;
	}
	/**
	 * @param gameMapping the gameMapping to set
	 */
	public void setGameMapping(List<GameMapping> gameMapping) {
		this.gameMapping = gameMapping;
	}
	/**
	 * @return the questMapping
	 */
	public List<QuestMapping> getQuestMapping() {
		return questMapping;
	}
	/**
	 * @param questMapping the questMapping to set
	 */
	public void setQuestMapping(List<QuestMapping> questMapping) {
		this.questMapping = questMapping;
	}
	/**
	 * @return the achievementMapping
	 */
	public List<AchievementMapping> getAchievementMapping() {
		return achievementMapping;
	}
	/**
	 * @param achievementMapping the achievementMapping to set
	 */
	public void setAchievementMapping(List<AchievementMapping> achievementMapping) {
		this.achievementMapping = achievementMapping;
	}
	/**
	 * @return the badgeMapping
	 */
	public List<BadgeMapping> getBadgeMapping() {
		return badgeMapping;
	}
	/**
	 * @param badgeMapping the badgeMapping to set
	 */
	public void setBadgeMapping(List<BadgeMapping> badgeMapping) {
		this.badgeMapping = badgeMapping;
	}
	/**
	 * @return the actionMapping
	 */
	public List<ActionMapping> getActionMapping() {
		return actionMapping;
	}
	/**
	 * @param actionMapping the actionMapping to set
	 */
	public void setActionMapping(List<ActionMapping> actionMapping) {
		this.actionMapping = actionMapping;
	}
	/**
	 * @return the levelMapping
	 */
	public List<LevelMapping> getLevelMapping() {
		return levelMapping;
	}
	/**
	 * @param levelMapping the levelMapping to set
	 */
	public void setLevelMapping(List<LevelMapping> levelMapping) {
		this.levelMapping = levelMapping;
	}
	/**
	 * @return the streakMapping
	 */
	public List<StreakMapping> getStreakMapping() {
		return streakMapping;
	}
	/**
	 * @param streakMapping the streakMapping to set
	 */
	public void setStreakMapping(List<StreakMapping> streakMapping) {
		this.streakMapping = streakMapping;
	}
}
